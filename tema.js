const wordsES = ['Full-Stack','Next.js','Supabase','Node.js'];
const wordsEN = ['Full-Stack','Next.js','Supabase','Node.js'];

document.addEventListener('DOMContentLoaded',()=>{
  setTimeout(()=>{ if(typeof lucide!=='undefined') lucide.createIcons(); },30);

  const html      = document.documentElement;
  const themeBtn  = document.getElementById('theme-btn');
  const themeIco  = document.getElementById('theme-ico');
  const langBtn   = document.getElementById('lang-btn');
  const langLbl   = document.getElementById('lang-lbl');
  const burger    = document.getElementById('burger');
  const navLinks  = document.getElementById('nav-links');
  const twEl      = document.getElementById('tw');
  const yearEl    = document.getElementById('fyear');

  if(yearEl) yearEl.textContent = new Date().getFullYear();

  // Theme
  const savedTheme = localStorage.getItem('de-theme')||'dark';
  html.setAttribute('data-theme', savedTheme);
  setThemeIcon(savedTheme);

  themeBtn&&themeBtn.addEventListener('click',()=>{
    const n = html.getAttribute('data-theme')==='dark'?'light':'dark';
    html.setAttribute('data-theme',n);
    localStorage.setItem('de-theme',n);
    setThemeIcon(n);
  });

  function setThemeIcon(t){
    if(!themeIco) return;
    themeIco.setAttribute('data-lucide', t==='dark'?'sun':'moon');
    if(typeof lucide!=='undefined') lucide.createIcons();
  }

  // Language
  let lang = localStorage.getItem('de-lang')||'es';
  applyLang(lang);

  langBtn&&langBtn.addEventListener('click',()=>{
    lang = lang==='es'?'en':'es';
    localStorage.setItem('de-lang',lang);
    applyLang(lang);
    restartTw();
  });

  function applyLang(l){
    html.setAttribute('data-lang',l);
    if(langLbl) langLbl.textContent = l==='es'?'EN':'ES';
    document.querySelectorAll('[data-i18n]').forEach(el=>{
      const k = el.getAttribute('data-i18n');
      if(T[l][k]!==undefined) el.innerHTML = T[l][k];
    });
    document.title = l==='es'
      ?'Dilan Espinosa — Desarrollador Full-Stack'
      :'Dilan Espinosa — Full-Stack Developer';
  }

  // Hamburger
  burger&&burger.addEventListener('click',()=> navLinks.classList.toggle('open'));
  document.querySelectorAll('.nav-link').forEach(l=>l.addEventListener('click',()=> navLinks.classList.remove('open')));

  // Typewriter
  let idx=0,ch=0,del=false,timer=null;
  function words(){ return lang==='es'?wordsES:wordsEN; }
  function twStep(){
    const w=words()[idx];
    if(!del){ ch++; if(twEl) twEl.textContent=w.slice(0,ch);
      if(ch===w.length){ del=true; timer=setTimeout(twStep,1800); return; }
      timer=setTimeout(twStep,85);
    } else { ch--; if(twEl) twEl.textContent=w.slice(0,ch);
      if(ch===0){ del=false; idx=(idx+1)%words().length; timer=setTimeout(twStep,350); return; }
      timer=setTimeout(twStep,45);
    }
  }
  function restartTw(){ clearTimeout(timer); ch=0;idx=0;del=false; if(twEl)twEl.textContent=''; timer=setTimeout(twStep,400); }
  restartTw();

  // Scroll animations
  const obs = new IntersectionObserver(entries=>{
    entries.forEach(e=>{ if(e.isIntersecting){ e.target.classList.add('in'); obs.unobserve(e.target); } });
  },{threshold:.1});
  document.querySelectorAll('.fade').forEach(el=>obs.observe(el));

  // Active nav
  const sections = document.querySelectorAll('section[id]');
  const links    = document.querySelectorAll('.nav-link');
  const navObs = new IntersectionObserver(entries=>{
    entries.forEach(e=>{
      if(e.isIntersecting){
        links.forEach(l=>l.classList.remove('active'));
        const a=document.querySelector(`.nav-link[href="#${e.target.id}"]`);
        if(a) a.classList.add('active');
      }
    });
  },{threshold:.35});
  sections.forEach(s=>navObs.observe(s));
});